package demo05_生产者消费者_改进版_等待唤醒机制;

public class SetStudent implements Runnable {
	private Student s;
	private int x = 0;

	public SetStudent(Student s) {
		this.s = s;
	}

	@Override
	public void run() {
		while (true) {
			synchronized(s) {
				// 判断有没有
				if (s.flag) {
					try {
						s.wait(); // t1等待，释放锁
					} catch (InterruptedException e) {
						e.printStackTrace();
					}
				}
				if (x % 2 == 0) {
					s.name = "林青霞";
					s.age = 27;
				} else {
					s.name = "刘意";
					s.age = 30;
				}
				x++; // x=1
				
				// 修改标记
				s.flag = true;
				// 唤醒线程
				s.notify(); // 唤醒t2，唤醒不表示你立马可以执行，必须还得抢CPU执行权
			}
			// t1有，或者t2有
		}

	}

}
