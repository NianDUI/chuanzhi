package demo06_卖电影票案例1;

public class SellTicket extends Thread {
//	// 定义100张票。
//	private int tickets = 100;
	// 为了让更多的线程对象共享着100张票，我们应该应用静态修饰。
	private static int tickets = 100;
	
	@Override
	public void run() {
		// 定义100张票。
		// 每个线程进来都会走这里，这样的话每个线程对象相当于卖的是自己的那100张票，这不合理。所以应该定义到外面。
//		int tickets = 100;

		// 为了模拟一直有票。
		while (true) {
			if (tickets > 0) {
				System.out.println(getName() + "正在出售第" + (tickets--) + "张票");
			} else {
				break;
			}
		}
	}
}
